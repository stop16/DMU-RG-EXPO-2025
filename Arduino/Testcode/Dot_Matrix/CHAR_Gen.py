# 도트 찍는 사이트: https://dotmatrixtool.com/

text = [
0x00, 0x00, 0x00, 0x00, 0x7f, 0x49, 0x49, 0x49, 0x49, 0x49, 0x49, 0x49,
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x64, 0x94, 0x94, 0x94,
    0x97, 0x94, 0x94, 0x64, 0x00, 0x00, 0x00, 0x00
]

# 짝수와 홀수 인덱스의 값을 2바이트로 합칩니다.
result = [(text[i+1] << 8) | text[i] for i in range(0, len(text), 2)]

# 각 결과를 '0x'로 시작하는 4자리 16진수 문자열로 변환합니다.
formatted_result = [f'0x{x:04x}' for x in result]

# 리스트를 쉼표와 공백으로 구분된 하나의 문자열로 합쳐서 출력합니다.
print(', '.join(formatted_result))